#!/usr/bin/env python3
"""Reports v1 CLI — read-only generator for the four canonical report
types (ADR-023 F6).

Usage
-----

    python3 scripts/generate_report.py validate <path>
    python3 scripts/generate_report.py example release  > out.json
    python3 scripts/generate_report.py example drift    > out.json
    python3 scripts/generate_report.py example training > out.json
    python3 scripts/generate_report.py example incident > out.json

Modes
-----

* ``validate`` — load a JSON file and validate it against the schema
  (lightweight in-tree check + optional jsonschema if installed).
  Exit 0 on success, 1 on any violation.
* ``example`` — emit a syntactically valid example for the chosen
  type. Useful for adopters wiring CI gates ("does my CI even know
  what a release report looks like?").

Why ``example`` instead of ``generate``
---------------------------------------

Real reports must be generated by the workflows that own the data
(release-checklist, drift-detection, retrain, incident). A generic
``generate`` here would invite the agent to invent values it does
not know — exactly the anti-pattern ADR-023 §F6 forbids. The CLI
is therefore restricted to the read-only ``validate`` and the
documentation-only ``example`` paths.

This script never writes outside the path the human passes on the
command line. It never reaches the network.
"""

from __future__ import annotations

import argparse
import json
import sys
from datetime import datetime, timezone
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
SCHEMA_FILE = REPO_ROOT / "templates" / "config" / "report_schema.json"

# Make `templates/common_utils` importable without installing a package.
sys.path.insert(0, str(REPO_ROOT / "templates"))

# pylint: disable=wrong-import-position
from common_utils.agent_context import AgentMode, Environment  # noqa: E402
from common_utils.reports import (  # noqa: E402
    DriftFeature,
    DriftPayload,
    IncidentPayload,
    QualityGate,
    ReleasePayload,
    TimelineEvent,
    TrainingPayload,
    build_drift_report,
    build_incident_report,
    build_release_report,
    build_training_report,
    load_report,
    validate_report_dict,
)


def _validate(path: Path) -> int:
    if not path.exists():
        print(f"error: file not found: {path}", file=sys.stderr)
        return 1
    try:
        doc = load_report(path)
    except (ValueError, json.JSONDecodeError) as exc:
        print(f"[FAIL] {path}: {exc}", file=sys.stderr)
        return 1

    # Optional second pass via jsonschema if available — gives more
    # specific error messages than the in-tree validator.
    try:
        import jsonschema  # type: ignore
    except ImportError:
        jsonschema = None  # type: ignore

    if jsonschema is not None:
        try:
            schema = json.loads(SCHEMA_FILE.read_text(encoding="utf-8"))
            jsonschema.validate(instance=doc, schema=schema)
        except jsonschema.ValidationError as exc:  # type: ignore[attr-defined]
            print(f"[FAIL] {path}: schema violation: {exc.message}", file=sys.stderr)
            print(f"  path: {list(exc.absolute_path)}", file=sys.stderr)
            return 1

    print(f"[ OK ] {path}")
    return 0



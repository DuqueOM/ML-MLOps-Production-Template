_EXAMPLES = {
    "release": _example_release,
    "drift": _example_drift,
    "training": _example_training,
    "incident": _example_incident,
}


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    sub = parser.add_subparsers(dest="cmd", required=True)

    p_val = sub.add_parser("validate", help="validate a report JSON file")
    p_val.add_argument("path", type=Path)

    p_ex = sub.add_parser("example", help="print a syntactically valid example")
    p_ex.add_argument("type", choices=sorted(_EXAMPLES))

    args = parser.parse_args()
    if args.cmd == "validate":
        return _validate(args.path)
    if args.cmd == "example":
        doc = _EXAMPLES[args.type]()
        validate_report_dict(doc)  # belt-and-suspenders
        print(json.dumps(doc, indent=2))
        return 0
    return 2  # pragma: no cover - argparse already enforces


if __name__ == "__main__":
    raise SystemExit(main())
